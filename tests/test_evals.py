# 🧪 LLM-as-a-Judge Evaluation Tests
# This file implements the "LLM-as-a-Judge" pattern, a critical quality gate
# for ensuring agent output is reliable and high-quality, as mandated by the framework.
# Reference: agent.md - The System Kernel for AI behavior and rules.
# Reference: workflow/02_COMPLETE_GUIDE.md (Section 6: Testing Strategy & LLM Evals)

import pytest
from app.factories.llm_factory import LLMFactory
from app.agents.interviewer import InterviewerAgent
from app.config import config
from langchain_core.prompts import ChatPromptTemplate
from langchain_core.output_parsers import StrOutputParser

@pytest.mark.asyncio
async def test_interviewer_question_quality():
    """
    Evaluates the quality of questions generated by the Interviewer Agent.
    Uses an LLM Judge to ensure questions are relevant, clear, and non-redundant.
    """
    
    # 1. Setup the Agent under test
    interviewer = InterviewerAgent()
    
    # Mock inputs
    user_input = "We need a new CRM system for the marketing team."
    context_summary = "Project: CRM Implementation. User: Sarah. Stakeholders: Marketing VP."
    whispers = [
        {"agent_name": "InfoSec", "priority": 9, "question": "Will this CRM store PII?", "analysis": "High risk"},
        {"agent_name": "IT Specialist", "priority": 8, "question": "Cloud or On-Prem?", "analysis": "Integration concern"}
    ]
    recent_history = "User: Hi\nBot: Hello, what project are we discussing?"

    # 2. Run the Agent
    agent_response = await interviewer.run(
        user_input=user_input,
        whispers=whispers,
        context_summary=context_summary,
        recent_history=recent_history
    )
    
    print(f"\nAgent Response: {agent_response}\n")

    # 3. Setup the LLM Judge (using primary model from factory)
    judge_llm = LLMFactory.get_llm("primary")
    
    judge_prompt = ChatPromptTemplate.from_messages([
        ("system", """
        You are an expert QA Judge for an AI Interviewer system.
        Evaluate the Agent Response based on the following criteria:
        1. Relevance: Does it address the user's input?
        2. Clarity: Is the question easy to understand?
        3. Prioritization: Did it incorporate the highest priority whisper (InfoSec: PII)?
        4. Tone: Is it professional and helpful?
        
        Input:
        - User Input: {user_input}
        - Highest Priority Whisper: {top_whisper}
        - Agent Response: {agent_response}
        
        Output ONLY 'PASS' if all criteria are met. Otherwise, output 'FAIL' followed by a brief reason.
        """),
        ("user", "Evaluate this response.")
    ])
    
    judge_chain = judge_prompt | judge_llm | StrOutputParser()
    
    # 4. Run the Evaluation
    eval_result = await judge_chain.ainvoke({
        "user_input": user_input,
        "top_whisper": "Will this CRM store PII?",
        "agent_response": agent_response
    })
    
    print(f"Judge Verdict: {eval_result}")
    
    # 5. Assert Success
    assert "PASS" in eval_result.upper(), f"LLM Judge Failed the response: {eval_result}"
